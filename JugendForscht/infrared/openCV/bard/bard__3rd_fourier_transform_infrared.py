import cv2
import numpy as np

# Load the infrared image
img = cv2.imread("cat.jpg", cv2.IMREAD_GRAYSCALE)

# Compute the FT of the image
f_img = cv2.fft2(img)

# Shift the zero-frequency component to the center of the array
shifted_f_img = np.fft.fftshift(f_img)

# Display the FT image
cv2.imshow("Fourier Transform", shifted_f_img)
cv2.waitKey(0)

