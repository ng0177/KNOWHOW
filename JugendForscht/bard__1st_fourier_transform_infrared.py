import cv2
import numpy as np

# Load the infrared image
image = cv2.imread("cat.jpg", cv2.IMREAD_GRAYSCALE)

# Pad the image with zeros
image = cv2.copyMakeBorder(image, top=0, bottom=0, left=0, right=0, borderType=cv2.BORDER_CONSTANT, value=0)

# Apply the Fourier transform algorithm
fourier_transform = cv2.dft(image, flags=cv2.DFT_COMPLEX_OUTPUT)

# Shift the zero-frequency component to the center of the image
fourier_transform = np.fft.fftshift(fourier_transform)

# Display the magnitude of the Fourier transform image
magnitude = np.abs(fourier_transform)
cv2.imshow("Magnitude of the Fourier Transform", magnitude)
cv2.waitKey(0)

